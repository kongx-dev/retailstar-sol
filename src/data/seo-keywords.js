// Helper functions for SEO operations
export const getKeywordsByTarget = (target) => {
  return keywordMap.filter(item => item.target === target);
};

export const getKeywordsByPersona = (persona) => {
  return keywordMap.filter(item => item.persona === persona);
};

export const getKeywordsByIntent = (intent) => {
  return keywordMap.filter(item => item.intent === intent);
};

export const generateMetaKeywords = (target) => {
  const keywords = getKeywordsByTarget(target);
  return keywords.map(item => item.keyword).join(', ');
};

export const generateMetaDescription = (target) => {
  const keywords = getKeywordsByTarget(target);
  const exploreKeywords = keywords.filter(item => item.intent === 'explore');
  const learnKeywords = keywords.filter(item => item.intent === 'learn');
  
  let description = `Discover ${target} - your gateway to the Solana ecosystem. `;
  
  if (exploreKeywords.length > 0) {
    description += `Explore ${exploreKeywords[0].keyword.toLowerCase()}. `;
  }
  
  if (learnKeywords.length > 0) {
    description += `Learn ${learnKeywords[0].keyword.toLowerCase()}. `;
  }
  
  description += `Join the Retailverse and unlock the future of Web3.`;
  
  return description;
};

export const generatePageTitle = (target, pageType = 'main') => {
  const keywords = getKeywordsByTarget(target);
  const domainName = target.replace('.sol', '');
  
  if (pageType === 'main') {
    return `${domainName}.sol - Solana Domain & Web3 Hub`;
  }
  
  const relevantKeyword = keywords.find(item => item.use_case.includes(pageType));
  if (relevantKeyword) {
    return `${relevantKeyword.keyword} | ${domainName}.sol`;
  }
  
  return `${domainName}.sol - ${pageType.charAt(0).toUpperCase() + pageType.slice(1)}`;
};

export const generateJSONLD = (target) => {
  const keywords = getKeywordsByTarget(target);
  const domainName = target.replace('.sol', '');
  
  return {
    "@context": "https://schema.org",
    "@type": "WebSite",
    "name": `${domainName}.sol`,
    "url": `https://${target}`,
    "description": generateMetaDescription(target),
    "keywords": generateMetaKeywords(target),
    "about": keywords.map(item => ({
      "@type": "Thing",
      "name": item.keyword,
      "description": item.use_case
    })),
    "audience": keywords.map(item => ({
      "@type": "Audience",
      "name": item.persona,
      "audienceType": item.intent
    }))
  };
}; 